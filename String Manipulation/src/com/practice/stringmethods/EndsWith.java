package com.practice.stringmethods;

public class EndsWith {

	public static void main(String[] args) {
		String str = new String("EndsWith is a Method inside String Class");
		System.out.println(str.endsWith("Class"));
		System.out.println(str.endsWith("class"));
		System.out.println(str.endsWith("ass"));
		System.out.println(str.endsWith("s"));
		

	}

}
